{
    return b == 0 ? a : gcd(b, a % b);
}
lol lcm(lol a, lol b) { return a / gcd(a, b) * b; }

lol pow(lol b, lol p, lol m)
{ // O(log(p) base 2)
    if (p == 0)
        return 1;

    lol sq = pow(b, p / 2, m);

    sq = ((sq % m) * (sq % m)) % m;

    if (p % 2 == 1)
        sq = ((sq % m) * (b % m)) % m;

    return sq;
}

